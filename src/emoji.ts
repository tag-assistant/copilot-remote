const FALLBACK_MAP: Record<string, string> = {
  '💭': '🤔',
  '🧠': '🤔',
  '✅': '👍',
  '❌': '👎',
  '🚀': '⚡',
  '💡': '🔥',
  '🔧': '👨‍💻',
  '📖': '👀',
  '✏️': '👨‍💻',
  '🌐': '⚡',
  '🔌': '⚡',
};

export function toTelegramReaction(emoji: string): string {
  if (TELEGRAM_REACTION_EMOJIS.has(emoji)) return emoji;
  return FALLBACK_MAP[emoji] ?? '👀';
}
